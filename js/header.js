$(function(){
    $('.hoverThing1').hover(function() {
        $('#subMenu1').css({
            'height': '250',
        });                    
    }, function() {
        $('#subMenu1').css({
            'height': '0',
        });
    });
});
$(function(){
    $('.hoverThing1').hover(function() {
        $('#btn1').css('background', '#efefef');
        $('#subMenu1').css('background', '#efefef');
    }, function() {
        $('#btn1').css('background', '#dddddd');
        $('#subMenu1').css('background', '#dddddd');
    });
})
